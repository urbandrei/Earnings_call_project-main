_FS_LABEL = {
    "egemaps": "eGeMAPS",
    "wavlm": "WavLM",
    "emotion2vec": "emotion2vec+",
    "wavlm_egemaps": "WavLM+eGeMAPS",
    "wavlm_text": "WavLM+text",
}
_COV_LABEL = {"audio": "", "audio_pastvol": "+vol"}


def _table3_label(model: str) -> str:
    head, rest = model.split("_", 1)
    for fs in sorted(_FS_LABEL, key=len, reverse=True):
        if rest.startswith(fs + "_"):
            cov = rest[len(fs) + 1 :]
            tag = _COV_LABEL.get(cov, cov)
            return f"{head} {_FS_LABEL[fs]}{(' ' + tag) if tag else ''}"
    return model


TABLE3_DM_COL = "dm_p_vs_stage1"  # headline: does audio beat the identity/vol floor?


